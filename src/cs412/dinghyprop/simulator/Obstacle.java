package cs412.dinghyprop.simulator;

/**
*  This class is a subclass of Point and allows for
*  Obstacle objects to be created.
*/
public class Obstacle extends Point{

	/**
	*  The constructor that sets up the position
	*  of the obstacle.
	*  @param x The X position of the obstacle.
	*  @param y The Y position of the obstacle.
	*/
	public Obstacle(int x, int y) {
		super(x, y);
	}
}
